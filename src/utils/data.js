// convert timestamp to time in hours and minutes from data
function convertTimeHoursMinutes(timestamp) {
  let date = new Date(timestamp);
  let hours = date.getHours();
  let minutes = date.getMinutes();
  minutes = Math.round(minutes / 5) * 5;
  if (minutes === 60) {
    hours++;
    minutes = 0;
  }
  if (minutes < 10) {
    minutes = "0" + minutes;
  }
  if (hours < 10) {
    hours = "0" + hours;
  }
  return hours + ":" + minutes;
}


